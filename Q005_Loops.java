public class Q005_Loops {
    public static void main(String[] args) {
        int i = 1;

        while(i < 11){
            System.out.print(i +"  ");
            i++;
        }

        System.out.println();

        for(int j = 11; j < 21; j++){
            System.out.print(j +" ");
        }
    }
}
